// init: The initial user-level program

#include "types.h"
#include "stat.h"
#include "user.h"
#include "fcntl.h"

char *argv[] = { "sh", 0 };

void
print_mod(){
  #ifdef NONE
    printf(1,"NONE");
  #endif
  #ifdef SCFIFO
    printf(1,"SCFIFO");
  #endif
  #ifdef NFUA
    printf(1,"NFUA");
  #endif
  #ifdef LAPA
    printf(1,"LAPA");
  #endif
  printf(1,"\n");
}

int
main(void)
{
  int pid, wpid;

  if(open("console", O_RDWR) < 0){
    mknod("console", 1, 1);
    open("console", O_RDWR);
  }
  dup(0);  // stdout
  dup(0);  // stderr

  for(;;){
    printf(1, "init: starting sh ");
    print_mod();
    pid = fork();
    if(pid < 0){
      printf(1, "init: fork failed\n");
      exit();
    }
    if(pid == 0){
      exec("sh", argv);
      printf(1, "init: exec sh failed\n");
      exit();
    }
    while((wpid=wait()) >= 0 && wpid != pid)
      printf(1, "zombie!\n");
  }
}
